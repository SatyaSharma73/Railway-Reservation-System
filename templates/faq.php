<h2>Travellers often have a lot of questions about booking train tickets, train status and timings, cancellations, refund and more. Given below are answers to all questions related to Indian Railway Train Tickets.</h2>
<div class="main-page" >
<div class="main-page" >
<div class="main-page" >
  <br>

<h3 class="faq-head">FAQs on Indian Railway Train Tickets:</h3>
<hr>
<div class="faq-container faq-wrap" itemscope="" itemtype="http://schema.org/FAQPage">
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: How early can I book Indian Railway train tickets?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> The Advance Reservation Period (ARP) for all classes of General Quota tickets is 120 days prior to the date of your journey. In the case of Tatkal and Premium Tatkal tickets, bookings are open only one day prior to the date of journey. This is applicable for tickets booked online as well as through the PRS counters at stations.  </p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: Can I book train tickets on the date of journey?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> Yes. If seats/berths are available on a train, you can book tickets &#8211; both General Quota tickets and Tatkal Quota tickets &#8211; till the chart preparation. Typically, the first chart is prepared about 4 hours prior to the scheduled departure of the train from the originating station.</p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: What is current train ticket booking?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> Current booking is a type of train ticket reservation where passengers are allowed to book tickets in a train after the preparation of the first chart, subject to the availability of seats. Current reservation can only be done online and this facility is stopped 30 minutes before the train’s departure so that the final chart can be prepared.</p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: What is the booking time window for Tatkal tickets?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> Tatkal ticket bookings for AC classes start at 10:00 am on the day prior to the date of journey. For non-AC classes such as Sleeper Class (SL) and reserved Second Seater (SS), Tatkal reservation opens at 11:00 am onwards. This is applicable for individuals (not agents) booking Tatkal tickets through their IRCTC accounts or through reservation counters.</p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: Can I book train tickets through the IRCTC website at night?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> Yes. However, you cannot book tickets through IRCTC website and app from 11:45 pm to 12:20 am (IST), every night.</p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: Is there a mobile app for booking IRCTC train tickets?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> Yes. IRCTC has an official mobile app for Android devices. It is called IRCTC Rail Connect and the app is available for download at Google Playstore. Currently, there is no official IRCTC train ticket booking app for iOS devices.</p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: What is the price difference between Tatkal tickets and General Quota Tickets?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> The base fare for both Tatkal tickets and General Quota Tickets remains the same. For booking Tatkal tickets, you have to pay additional Tatkal charges at the rate of 10% of the base fare for second class tickets and 30% of the base fare for all other travel classes. The Tatkal booking charges are also subject to minimum and maximum flat fare. <a href="https://www.fabhotels.com/blog/indian-railways/tatkal-railway-reservation/">Click here for more details</a>.</p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: How can I check the PNR status of my train ticket reservation?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> You can check the PNR status of your train ticket reservation through IRCTC by going to IRCTC website> Trains > PNR Enquiry> Enter PNR Number. Train ticket status can also be checked through the IRCTC mobile app, Indian Railways Passenger Enquiry website, third-party websites and apps, etc. You can also check the PNR status by sending an SMS (Format: PNR <10 digit PNR Number >) to 139 or by dialing 139 from your phone. To know more about <a href="https://www.fabhotels.com/blog/indian-railways/PNR-STATUS/">how to check PNR status, click here</a>.</p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: What is the maximum number of passengers allowed per train ticket?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> A maximum of 6 passengers are allowed per General Quota tickets and a maximum of 4 passengers per Tatkal Quota tickets.</p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: Can I transfer my train ticket to a friend?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> No. Indian Railways allows you to transfer your train ticket only to a family member. Also, you cannot transfer your e-Tickets online through IRCTC. In order to transfer the ticket to a family member, you must go to a Railway Reservation Counter at least 24 hours prior to the scheduled departure time of the train. You must also carry a printout of your Electronic Reservation Slip (ERS) and a valid ID proof of the transferee.</p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: How can I cancel my train ticket after chart preparation?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> Whether you have a counter ticket or e-Ticket, it can be cancelled after the chart preparation only by filing a Ticket Deposit Receipt aka TDR. For confirmed tickets, TDR must be filed at least 4 hours prior to the train’s scheduled journey commencement. For RAC/Waitlisted tickets, TDR has to be filed 30 minutes prior to the scheduled departure of the train. To know more about <a href="https://www.fabhotels.com/blog/indian-railways/irctc-cancellation-rules-and-policies/">canceling tickets after chart preparation, click here</a>.</p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: Do I get any concession on Tatkal tickets?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> No. Neither Tatkal nor Premium Tatkal allows any kind of travel concession including the concession for senior citizens.</p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: Can I book Tatkal tickets for all travel classes?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> You can book Tatkal tickets for all AC and non-AC reserved travel classes except for First Class AC (1 AC).</p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: How can I check the train time table and seat availability online?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> Go to the IRCTC website, enter the names/codes of the source station and destination station, and choose the date of journey. This will give you a complete time table of the trains running between the two stations you have queried for and the number of seats available on each. To know more about train time table and <a href="https://www.fabhotels.com/blog/indian-railways/seat-availability/">seat availability through websites, SMS, call, etc. click here</a>.</p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: Can I book train tickets through third-party apps?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> Yes. There are several third-party apps and websites that allow you to book train tickets online. However, even these sites do the booking through IRCTC, which is the only official online ticketing partner of the Indian Railways.</p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: Is counter ticket booking available at night?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> You can reserve your tickets through Indian Railways booking counters from 8:00 in the morning till 8:00 in the night, on all days except Sundays. On Sundays, the booking centers are open from 8:00 in the morning till 2:00 in the noon. </p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: Can I board a train with a Waitlisted ticket?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> No. If the status of your ticket is Waitlisted even after chart preparation, it is recommended that you don’t board the train.</p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: Can I cancel counter tickets online?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> Yes. <a href="https://www.operations.irctc.co.in/ctcan/SystemTktCanLogin.jsf" target="_blank" rel="nofollow">IRCTC has a counter ticket cancellation page</a> where you can cancel your paper tickets without having to rush to a counter. This facility is available only if you have given a mobile number at the time of ticket reservation. Moreover, you must collect the refund either from the journey station or from one of the nearby PRS counters by surrendering the ticket within the stipulated time limit.</p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: What are RAC train tickets? Can I board a train with a RAC ticket?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> The term RAC stands for Reservation against Cancellation. Passengers with RAC tickets don’t have a completely confirmed berth. Instead, they have to share the berth with another passenger. After the commencement of the journey, RAC passengers might be allotted full berths by the ticket examiner if there are any cancellations or no-shows.</p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: Can I cancel my train ticket partially?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> Yes. In the case of partial cancellations of e-Tickets, you will get a fresh ERS. You will be refunded for the cancelled berths/seats while other passengers on the ticket can travel with the fresh ERS.</p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: How can I make sure that all passengers on a ticket are allotted seats in the same coach?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> Seats are allocated to passengers based on availability, so there is no guarantee that all passengers will be in the same coach. However, in IRCTC, there is an option to ‘book tickets only if berths are allotted in the same coach’. If you choose this option, tickets will not be booked if there aren’t enough berths in one coach for all passengers. </p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: Will I get a refund if I cancel my confirmed Tatkal ticket?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> No. You are not entitled to any refund if you choose to cancel a confirmed Tatkal or Premium Tatkal ticket. However, you will get a full refund for confirmed Tatkal tickets if the train is cancelled, delayed by more than 3 hours, diverted, etc. Click here to learn more about the refund rules for Tatkal tickets. (Link to the Refund Page)</p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: My train ticket status is Waitlisted even after chart preparation. Should I cancel the ticket through IRCTC to get the refund?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> No. If the status of your e-Ticket is Waitlisted even after the preparation of the chart, it will be automatically cancelled and the refund process will be initiated by IRCTC.</p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: Should I carry a printout of my e-Ticket while travelling?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> Not at all. You can travel with a screenshot of the ERS, Virtual Reservation Messages or the SMS sent to your registered mobile number. It is mandatory that you carry a valid government photo ID along with the ERS/VRM/SMS.</p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: Are there any concessions allowed on train tickets?  </strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> Yes. All male senior citizens and female senior citizens will get a concession of 40% and 50% of the fare, respectively, by opting for Senior Citizen Concession. Passengers who are differently abled and journalists can also opt for concession provided that they possess the ID card issued by the Railways.</p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: Are there any concessions for bulk train ticket bookings?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> No</p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: How are Premium Tatkal tickets different from Tatkal tickets?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> The main difference between them is in the pricing system. PT tickets follow a dynamic pricing system where the ticket prices go up based on demand. The Tatkal ticket prices, however, are fixed at 10% to 30% of the base fare subject to minimum and maximum flat charges. To learn more about the <a href="https://www.fabhotels.com/blog/indian-railways/tatkal-railway-reservation/">difference between Tatkal and Premium Tatkal Tickets, click here</a>.</p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: Is there any limit to the number of tickets I can book through my IRCTC account?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> An individual user can book 6 tickets per month per account. This can be extended to 12 tickets per month by linking the account with Aadhaar card.</p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: The current status of my train ticket is WL60/CNF. What does this mean?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> The first part of the status means that you were the 60th passenger on the Waitlist at the time of booking. The second part of the ticket status means that you have gone up in the list and currently, your reservation is confirmed. A seat number and coach number will be allocated to you after the chart preparation.</p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: Can I book Tatkal and Premium Tatkal tickets from PRS counters at stations?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> While Tatkal tickets can be booked from PRS counters at stations, Premium Tatkal tickets can only be booked online.</p>
</p></div>
</p></div>
<div itemprop="mainEntity" itemscope itemtype="http://schema.org/Question">
<p itemprop="name">
<strong>Q: Can international tourists and NRIs book train tickets in Indian trains?</strong></p>
<div itemprop="acceptedAnswer" itemscope="" itemtype="http://schema.org/Answer">
<p itemprop="text"><strong>A:</strong> Yes. IRCTC allows foreign and NRI travelers with a valid passport to book train tickets under the Foreign Tourist Quota. International and NRI travelers must pay a registration fee of ₹ 100 + GST at the time of creating an account with IRCTC. A service charge will also be levied from them at the rate of ₹ 200 + GST per ticket.</p>
</p></div>
